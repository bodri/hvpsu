/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_TIM1_Init();
  MX_TIM8_Init();
  MX_ADC2_Init();
  MX_TIM2_Init();
  /* USER CODE BEGIN 2 */

  // Calibrate ADC
  HAL_ADCEx_Calibration_Start(&hadc2, ADC_SINGLE_ENDED);

  // Start Current and Voltage measurement
  HAL_ADC_Start_DMA(&hadc2, (uint32_t *)adc2Readings, 1);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  SET_BIT(TIM1->CR1, TIM_CR1_UDIS);
  TIM1->CCR1 = 0;
  TIM1->CCR2 = 399;
  CLEAR_BIT(TIM1->CR1, TIM_CR1_UDIS);

  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
  HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_1);

  SET_BIT(TIM8->CR1, TIM_CR1_UDIS);
  TIM8->CCR1 = 399;
  TIM8->CCR2 = 0;
  CLEAR_BIT(TIM8->CR1, TIM_CR1_UDIS);

  HAL_TIM_PWM_Start(&htim8, TIM_CHANNEL_1);
  HAL_TIMEx_PWMN_Start(&htim8, TIM_CHANNEL_1);

  HAL_TIM_Base_Start_IT(&htim2);
  while (1)
  {
	  if (loopTimeElapsed) {
		  float currentVoltage = ((((float)adc2Readings[0] * 3.3f) / 4096.0f) - 1.6f) * 2.0 * 3620000.0 / 20000.0f;

		  voltageError = targetVoltage - currentVoltage;
		  integral += voltageError * dt;

		  phase = kp * voltageError + ki * integral;
		  if (phase > 398) phase = 398;
		  if (phase < 0) phase = 0;

		  SET_BIT(TIM8->CR1, TIM_CR1_UDIS);
		  TIM8->CCR1 = 399 - phase;
		  TIM8->CCR2 = phase;
		  CLEAR_BIT(TIM8->CR1, TIM_CR1_UDIS);

		  loopTimeElapsed = false;
		  stepCounter++;
		  if (stepCounter == 10000) {
			  HAL_GPIO_TogglePin(STEP_GPIO_Port, STEP_Pin);
//			  if (targetVoltage == 50.0f)
//				  targetVoltage = 50.0f;
//			  else
//				  targetVoltage = 50.0f;
			  stepCounter = 0;
		  }
	  }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

